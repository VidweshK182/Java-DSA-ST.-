import java.util.*;
class Pattern2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        for(int i=1;i<=a;i++){
            char b = 'A';
            for(int j=1;j<=a;j++){
                System.out.print(b+" ");
                b++;
            }
        System.out.println();
        }
    }
}
